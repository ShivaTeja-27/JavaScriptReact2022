console.log("THIS IS Multiplication OF 3 NUMBERS")

function fnMultiply(x,y,z){

    var num1 = x;
    var num2 = y;
    var num3 = z;

    var result = num1 * num2 * num3;
    console.log(result);
}

fnMultiply(2,3,4);
fnMultiply(34,2,40);
fnMultiply(55,2,5);

console.log("THIS IS DIVISION OF 3 NUMBERS")

function fnDivide(a,b,c){
    var num1 = a;
    var num2 = b;
    var num3 = c;

    var result = num1 / num2 / num3;
    console.log(result);
}
fnDivide(2,3,4);
fnDivide(34,2,40);
fnDivide(55,2,5);

